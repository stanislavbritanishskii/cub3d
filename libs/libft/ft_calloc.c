#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*res;

	if (size && size * count / size != count)
		return (NULL);
	res = malloc(count * size);
	if (!res)
		return (NULL);
	ft_bzero(res, count * size);
	return (res);
}
